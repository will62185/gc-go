package main

import (
	"fmt"
)

func main(){
	// Declare and assign a value
	x := 42
	fmt.Println(x)
	// Already declared just assign x = 99
	x = 99
	fmt.Println(x)
	// This is a statement, 100 + 24, is an expression
	y := 100 + 24
	fmt.Println(y)
}